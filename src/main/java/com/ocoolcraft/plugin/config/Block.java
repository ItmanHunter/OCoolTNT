package com.ocoolcraft.plugin.config;

public class Block {
    private double destory, drop, fire;

    public double getDestory() {
        return destory;
    }

    public void setDestory(double destory) {
        this.destory = destory;
    }

    public double getDrop() {
        return drop;
    }

    public void setDrop(double drop) {
        this.drop = drop;
    }

    public double getFire() {
        return fire;
    }

    public void setFire(double fire) {
        this.fire = fire;
    }
}
