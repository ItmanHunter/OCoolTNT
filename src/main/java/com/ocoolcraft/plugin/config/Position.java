package com.ocoolcraft.plugin.config;

public class Position {
    public int getEventX() {
        return eventX;
    }

    public void setEventX(int eventX) {
        this.eventX = eventX;
    }

    public int getEventY() {
        return eventY;
    }

    public void setEventY(int eventY) {
        this.eventY = eventY;
    }

    public int getEventZ() {
        return eventZ;
    }

    public void setEventZ(int eventZ) {
        this.eventZ = eventZ;
    }

    private int eventX, eventY, eventZ;
}